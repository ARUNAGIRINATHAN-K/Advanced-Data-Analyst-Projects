CREATE TABLE employee_attrition (
    EmployeeID INT PRIMARY KEY,
    Age INT,
    Gender VARCHAR(10),
    Department VARCHAR(50),
    EducationLevel INT,
    JobRole VARCHAR(50),
    YearsAtCompany INT,
    MonthlyIncome FLOAT,
    JobSatisfaction INT,
    WorkLifeBalance INT,
    PerformanceRating INT,
    TrainingTimesLastYear INT,
    OverTime VARCHAR(5),
    DistanceFromHome INT,
    Attrition VARCHAR(5) 
);
